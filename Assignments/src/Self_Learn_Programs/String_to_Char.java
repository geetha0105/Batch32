package Self_Learn_Programs;

public class String_to_Char {
    public static void main(String[] args) {
        //Converting a String to Char

        String A = "Geetha";

        for (int i = 0 ; i< A.length(); i++){
            char ch =  A.charAt(i);
            System.out.println(ch);
        }

    }
}
